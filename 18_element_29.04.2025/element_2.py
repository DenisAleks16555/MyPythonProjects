# Шаг 1: Определяем множество
numbers = {10, 15, 20, 25, 30, 35, 40}

# Шаг 2: Удаляем числа, которые делятся на 10
# Мы создадим новый набор, чтобы избежать изменения множества во время итерации
numbers_to_remove = {num for num in numbers if num % 10 == 0}

# Удаляем найденные числа
for num in numbers_to_remove:
    numbers.remove(num)

# Шаг 3: Выводим измененное множество
print(numbers)  # Ожидаемый результат {15,25,35}
