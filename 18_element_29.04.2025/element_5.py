# Шаг 1: Определяем список
fruits = ["яблоко", "банан", "яблоко", "апельсин", "банан", "киви"]

# Шаг 2: Преобразуем список в множество, чтобы удалить дубликаты
unique_fruits = set(fruits)  # Преобразуем список в множество

# Шаг 3: Сортируем элементы и преобразуем обратно в список
sorted_fruits = sorted(unique_fruits)  # Сортируем множество и преобразуем в список

# Шаг 4: Выводим результат
print(sorted_fruits)  # Ожидаемый вывод: ['апельсин', 'банан', 'киви', 'яблоко']

 