def invert_dict(d):
    # Шаг 1: Создаем новый словарь для инвертированных пар
    inverted_dict = {}

    # Шаг 2: Перебираем элементы исходного словаря
    for key, value in d.items():
        # Шаг 3: Добавляем инвертированную пару в новый словарь
        inverted_dict[value] = key

    # Шаг 4: Возвращаем инвертированный словарь
    return inverted_dict

# Пример использования функции
d = {'a': 1, 'b': 2, 'c': 3}
print(invert_dict(d))  # Ожидаемый вывод: {1: 'a', 2: 'b', 3: 'c'}