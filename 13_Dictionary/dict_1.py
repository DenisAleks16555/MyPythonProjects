def merge_dicts(d1, d2):
    # Шаг 1: Создаем новый словарь
    merged_dict = {}

    # Шаг 2: Копируем все элементы из первого словаря
    for key in d1:
        merged_dict[key] = d1[key]

    # Шаг 3: Добавляем элементы из второго словаря
    for key in d2:
        merged_dict[key] = d2[key]  # Если ключ уже есть, значение будет обновлено

    # Шаг 4: Возвращаем объединенный словарь
    return merged_dict

# Пример использования функции
d1 = {'a': 1, 'b': 2}
d2 = {'b': 3, 'c': 4}
print(merge_dicts(d1, d2))  # Ожидаемый вывод: {'a': 1, 'b': 3, 'c': 4}