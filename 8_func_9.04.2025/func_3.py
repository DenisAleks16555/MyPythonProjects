def count_primes(numbers):  # Создаём функцию с именем count_primes, которая принимает список numbers
    count = 0  # Счётчик простых чисел, начинаем с 0

    for num in numbers:  # Для каждого числа num в списке numbers
        if is_prime(num):  # Проверяем, простое ли число (функция is_prime сделает эту проверку)
            count += 1  # Если простое, увеличиваем счётчик на 1

    return count  # Возвращаем количество простых чисел

def is_prime(n):  # Функция, которая проверяет, простое ли число n
    if n < 2:  # Числа меньше 2 не считаются простыми
        return False
    for i in range(2, n):  # Проверяем делится ли n на числа от 2 до n-1
        if n % i == 0:  # Если делится без остатка — число не простое
            return False
    return True  # Если ни на что не делится — число простое

# Пример использования:
my_list = [2, 4, 5, 6, 7, 9, 11]
print(count_primes(my_list))  # Выведет: 4 (потому что простые числа: 2, 5, 7, 11)