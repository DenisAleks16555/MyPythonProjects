# Шаг 1: Определяем функцию
def find_minimum(numbers):
    # Шаг 2: Проверяем, пустой ли список
    if not numbers:  # Если список пустой
        return None  # Возвращаем None (или можно вернуть сообщение об ошибке)

    # Шаг 3: Инициализируем переменную для хранения минимума
    minimum = numbers[0]  # Начальное значение минимума — первый элемент списка

    # Шаг 4: Перебираем элементы списка
    for number in numbers:
        if number < minimum:  # Если текущий элемент меньше текущего минимума
            minimum = number  # Обновляем минимум

    # Шаг 5: Возвращаем результат
    return minimum

# Пример использования функции
my_list = [5, 3, 8, 1, 4]  # Список целых чисел
result = find_minimum(my_list)  # Вызываем функцию
print(result)  # Ожидаемый вывод: 1