# Шаг 1: Определяем функцию
def multiply_elements(numbers):
    # Шаг 2: Инициализируем переменную для произведения
    product = 1  # Начальное значение произведения

    # Шаг 3: Перебираем элементы списка
    for number in numbers:
        product *= number  # Умножаем текущее произведение на элемент списка

    # Шаг 4: Возвращаем результат
    return product

# Пример использования функции
my_list = [2, 3, 4]  # Список целых чисел
result = multiply_elements(my_list)  # Вызываем функцию
print(result)  # Ожидаемый вывод: 24