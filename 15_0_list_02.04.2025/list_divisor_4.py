# Шаг 1: Ввод числа
n = int(input("Введите натуральное число n: "))

# Шаг 2: Создание списка делителей
divisors = []  # создаем пустой список для делителей

for i in range(1, n + 1):  # проходим по всем числам от 1 до n
    if n % i == 0:  # проверяем, является ли i делителем n
        divisors.append(i)  # добавляем делитель в список

# Шаг 3: Вывод результата
print("Делители числа", n, ":", divisors)