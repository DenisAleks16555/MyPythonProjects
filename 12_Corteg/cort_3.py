# Шаг 1: Определение кортежей
tuple1 = (1, 2, 3, 4, 5)
tuple2 = (1, 3, 3, 4, 6)
tuple3 = (1, 2, 0, 4, 7)

# Шаг 2: Инициализация списка для хранения совпадений
matches = []

# Шаг 3: Цикл по индексам
for i in range(min(len(tuple1), len(tuple2), len(tuple3))):
    # Шаг 4: Сравнение элементов
    if tuple1[i] == tuple2[i] == tuple3[i]:
        matches.append(tuple1[i])  # Можно использовать любой из кортежей, так как они равны

# Шаг 5: Вывод результата
print("Совпадающие элементы на одной и той же позиции:", matches)