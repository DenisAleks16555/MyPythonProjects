# Наш словарь
dictionary = {'apple': 'яблоко', 'dog': 'собака', 'book': 'книга'}

# Просим пользователя ввести ключ
key = input("Введите английское слово: ")

try:
    # Пытаемся получить значение по ключу
    translation = dictionary[key]
    print(f"Перевод слова '{key}': {translation}")

except KeyError:
    # Если ключа нет, выводим сообщение об ошибке
    print(f"Слово '{key}' не найдено в словаре.")