# Первое число (целое)
number = 42

# Вторая "число" (строка)
string_number = "5"

try:
    # Пытаемся сложить число и строку
    result = number + string_number
    # Если сложение прошло успешно, выводим результат
    print("Результат сложения:", result)

except TypeError:
    # Если возникла ошибка TypeError (несовместимость типов),
    # выводим сообщение об ошибке
    print("Ошибка: Нельзя сложить число и строку напрямую.")
    print("Нужно сначала преобразовать строку в число (если это необходимо).")